    func util_generateTable_divide() {
        var table: [(a: [Digit], b: [Digit], result: (q: [Digit], r: [Digit]), source: (a: UInt32, b: UInt16, q: UInt16, r: UInt16))] = []

        let D = UInt32(UInt8.max)
        let A: [UInt32] = [0, 1, D - 1, D * D + D - 1, D * D * D + D * D + D - 1, D * D * D * D + D * D * D + D * D - 1, D * D * D * D, UInt32.max]
        let B0: [UInt16] = [UInt32(1), 2, 3, 3 * D / 2, 2 * D + 1, 3 * D + 1, 4 * D / 5].map(UInt16.init)

        for a in A {
            var B: [UInt16] = B0 + [a, a &+ 1, a &- 1, a / 2].compactMap(UInt16.init(exactly:)).filter { $0 != 0 }
            B = Set(B).sorted()

            for b in B {
                let high = UInt16(a >> 16)
                let low = UInt16(a & 0xffff)
                let result = b.dividingFullWidth((high, low))


                let numberA: [UInt8] = [
                    UInt8((a >> (0 * 8)) & 0xff),
                    UInt8((a >> (1 * 8)) & 0xff),
                    UInt8((a >> (2 * 8)) & 0xff),
                    UInt8((a >> (3 * 8)) & 0xff),
                ]
                let numberB: [UInt8] = [
                    UInt8((b >> (0 * 8)) & 0xff),
                    UInt8((b >> (1 * 8)) & 0xff),
                ]
                let numberQ: [UInt8] = [
                    UInt8((result.quotient >> (0 * 8)) & 0xff),
                    UInt8((result.quotient >> (1 * 8)) & 0xff),
                ]
                let numberR: [UInt8] = [
                    UInt8((result.remainder >> (0 * 8)) & 0xff),
                    UInt8((result.remainder >> (1 * 8)) & 0xff),
                ]

                table.append((numberA, numberB, (numberQ, numberR), (a, b, result.quotient, result.remainder)))
            }
        }

        for (a, b, result, source) in table {
            print("(a: \(a), b: \(b), result: \(result)), // \(source.a) / \(source.b) = \(source.q) + \(source.r) / \(source.b)")
        }
    }
