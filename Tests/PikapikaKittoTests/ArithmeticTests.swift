    func util_generateTable_multiply() {
        let S: [Digit] = [0, 1, 128, 254, 255]
        var table: [[Digit]] = []
        for a0 in S {
            for a1 in S {
                for b0 in S {
                    for b1 in S {
                        let a = UInt16(a0) + UInt16(a1) * 1 << 8
                        let b = UInt16(b0) + UInt16(b1) * 1 << 8
                        let c = a.multipliedFullWidth(by: b)
                        let c0 = c.low & 0xff
                        let c1 = c.low >> 8
                        let c2 = c.high & 0xff
                        let c3 = c.high >> 8

                        let row = [a0, a1, b0, b1, Digit(c0), Digit(c1), Digit(c2), Digit(c3)]
                        table.append(row)
                    }
                }
            }
        }

        util_print_table(table)
    }
