    func test_addScalars() {
        // util_generateTable_addScalars()

        let table: [[Digit]] =
        [
        //  [a,    b,    c,  overflow]
            [0,    0,    0,    0,    ],
            [0,    1,    1,    0,    ],
            [0,    128,  128,  0,    ],
            [0,    254,  254,  0,    ],
            [0,    255,  255,  0,    ],
            [1,    0,    1,    0,    ],
            [1,    1,    2,    0,    ],
            [1,    128,  129,  0,    ],
            [1,    254,  255,  0,    ],
            [1,    255,  0,    1,    ],
            [128,  0,    128,  0,    ],
            [128,  1,    129,  0,    ],
            [128,  128,  0,    1,    ],
            [128,  254,  126,  1,    ],
            [128,  255,  127,  1,    ],
            [254,  0,    254,  0,    ],
            [254,  1,    255,  0,    ],
            [254,  128,  126,  1,    ],
            [254,  254,  252,  1,    ],
            [254,  255,  253,  1,    ],
            [255,  0,    255,  0,    ],
            [255,  1,    0,    1,    ],
            [255,  128,  127,  1,    ],
            [255,  254,  253,  1,    ],
            [255,  255,  254,  1,    ],
        ]

        for row in table {
            let sum = addScalars(row[0], row[1])
            XCTAssertEqual(sum.result, row[2], "\(row[0]) + \(row[1])")
            XCTAssertEqual(sum.overflow, row[3], "\(row[0]) + \(row[1])")
        }
    }

    func util_generateTable_addScalars() {
        let S: [UInt8] = [0, 1, 128, 254, 255]
        var table: [[Digit]] = []
        for a in S {
            for b in S {
                let result = a &+ b
                let overflow: Digit = result < a || result < b ? 1 : 0
                let row = [a, b, result, overflow]
                table.append(row)
            }
        }
        util_print_table(table)
    }

